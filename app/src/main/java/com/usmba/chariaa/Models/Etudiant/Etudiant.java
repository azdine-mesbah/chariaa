    public String getSeason(int season){
        String S ="مسجل بالفصل ";

        switch (season)
        {
            case 1:{
                if(inscriptionSoudassiya1 != null){
                    String[] s = inscriptionSoudassiya1.split("S");
                    for (String a:s) {
                        switch (a){
                            case "1": S += "الاول";break;
                            case "3": S += " - الثالث - ";break;
                            case "5": S += " - الخامس ";break;
                        }
                    }}
            }
            break;
            case 2:{
                if(inscriptionSoudassiya2 != null){
                    String[] s = inscriptionSoudassiya2.split("S");
                    for (String a:s) {
                        switch (a){
                            case "2": S += "الثاني";break;
                            case "4": S += " - الرابع";break;
                            case "6": S += " - السادس";break;
                        }
                    }}
            }
            break;
        }
        return S;
    }
